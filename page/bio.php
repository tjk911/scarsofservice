	<h4>About the reporters</h4>
	<img src="assets/img/Kirsti%20Marohn.jpg" class="left" style="padding:10px; max-width:200px"></img>
	<p><b>Kirsti Marohn</b> has been a reporter at the St. Cloud Times since 1998. She has covered local and state government, social issues and the environment. Her father was a U.S. Marine and her husband served in the Minnesota National Guard prior to 9/11.</p>

	<p>Marohn became interested in writing about veterans after hearing about the struggles many have faced since returning from Iraq and Afghanistan, and how community resources were not always adequate to help them.</p>

	<p>You can follow Marohn on <a href="http://www.facebook.com/sctimeskirsti">Facebook</a> and on Twitter @kirstimarohn.</p>

