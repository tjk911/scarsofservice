			    <p>Instead, the Nest provides layers of services for the veterans. They are offered peer-to-peer discussion groups, substance abuse counseling, art therapy, chiropractic treatment, spirituality and more.</p>

			    <p>“We give them a loving home, and they create their tactics in their world to complete their mission,” Butler said. “There isn’t a cure for PTSD. What works for one may not work for others. Some suffer from TBI (traumatic brain injury), some suffer from addiction. There’s a whole host of things that they suffer from. We just put the tools in place, and they help each other through all of those issues.”</p>

			    <p>Dianne Gates, a licensed alcohol and drug counselor with Sobriety First, works full time at the Nest. She offers chemical dependency counseling as well as some mental health treatment, since many of the veterans at the Nest have both chemical dependency and PTSD.</p>

			    <p>The majority of the chemical dependency cases she sees are veterans who are self-medicating. The problem might have started when they were younger, Gates said. Then when they went to war, “it just kind of exacerbates things.”</p>

			    <p>“And then they come home, and the government says, ‘OK, here you go. You’re home.’ And they go, ‘Um, I don’t know how to function in this world anymore.’ So they teach them a whole different mentality and a different way of survival.”</p>

			    <p>Gates said the veterans often have been prescribed several medications, including drugs for pain relief, mood stabilization and to help them sleep. Abuse of the medications is common, and they often don’t help the underlying problems, she said.</p>

			    <p>“You have to deal with the issues first,” Gates said. “It’s fine to give them medication that will help bring them up a little bit so they can deal with the issues. But then the purpose of the medications is to get them off, not just give them more and more and more.”</p>

