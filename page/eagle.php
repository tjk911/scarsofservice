			    <p>A year later, the Nest is home to 47 veterans, with more on a waiting list. They’ve served in Iraq, Afghanistan, Vietnam, Korea. Some stay a night or two. Others have been here for months and have no plans to leave.</p>

			    <div class="large-4 medium-6 column show-for-medium-up left" style="margin-right:10px;">
					<h2>“PTSD is a lifetime. Recovery is a lifetime. They need to have a safe place to continue that.”</h2>
				</div>

			    <p>Some struggle with substance abuse or post-traumatic stress disorder. Some tried to rejoin civilian society and failed miserably. Some found themselves in legal trouble or left behind troubled marriages.</p>

			    <p>“You hear that repeatedly, that they come back here, they can’t function in the civilian world,” Butler said. “They don’t know how to act or react in their families.”</p>

			    <p>Many say that without the Nest, they’d be dead. Maybe from alcohol or drugs, maybe by their own hand.</p>

			    <p>There are no rules about how long a veteran can stay. Butler considers that part of the red tape of insurance policies. Healing doesn’t always take place on schedule.</p>

			    <p>“Some veterans can go years without understanding the negative effect or making the connection or being able to admit that they may have an issue, because they’re trained not to do that,” she said.</p>

			    <p>“PTSD is a lifetime. Recovery is a lifetime. They need to have a safe place to continue that.”</p>

