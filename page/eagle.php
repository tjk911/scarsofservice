			    <p>Eagle’s Healing Nest leases two buildings on the campus. A third is being renovated with plans to add more rooms for veterans.</p>

			    <p>The veterans are responsible for taking care of the Nest. They cook, clean, do dishes, plan menus, do repairs and care for the animals, which include pigs, chickens, rabbits, a goat and horses.</p>

			    <p>“They take care of their own home,” Butler said. “It’s their home. It’s their gift. It is giving them purpose.”</p>

			    <p>The fee to stay at the Nest is $35 a day, but no one is turned away. They each have their own room, small but comfortable, decorated by volunteers.</p>

			    <p>There’s a room for quiet mediation or study, with a computer for job searches or online classes and an overflowing bookshelf. There’s a gathering room with stout leather chairs where support groups meet. Downstairs, there’s a pool table, a workout room, a woodworking shop filled with tools.</p>

			    <p>Always, there is talking — in discussion groups, at the breakfast table, smoking on the front porch, around a bonfire at night.</p>

