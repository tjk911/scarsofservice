			    <p>That was Sunday. By Monday, Butler was meeting with a college adviser and beginning the path to a degree in psychology.</p>

			    <p>“I called him and said, ‘In four years, I will give our veterans a place to heal with honor,’” she said.</p>

			    <p>But four years was too long. Butler heard about the rising number of suicides among veterans, as many as 22 dying by their own hand every day in the United States.</p>

			    <p>“I was tired of being told by our veterans, by our families, that the resources that are there — they’re being told they are full, or they can’t get in, or they wait weeks or months for appointments,” Butler said. “And they were taking their lives, and their families are being broken apart.”</p>

			    <p>She began to talk to friends and colleagues about starting a small home with eight to 10 beds where veterans could find sanctuary and peace while they healed.</p>

			    <p>A veteran told her about a former juvenile prison in Sauk Centre on acres of rolling farmland dotted with stately but aging buildings in need of repair. Butler knew as soon as she saw it that it was the right place.</p>

			    <p>A group had tried to start something for veterans on the property but never got it going.</p>

			    <p>“People are always looking for that pot of gold, that handout to do something,” Butler said. “I said, ‘We can’t wait for that. We can do this within ourselves.’”</p>

			    <p>She recruited volunteers, got a bank loan and a donation from an American Legion post. The volunteers started renovating two of the buildings and brought them up to code. Eagle’s Healing Nest was born.</p>

				
