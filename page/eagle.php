			    <p>Jamie Stowe served a year in Iraq with the Kansas National Guard. He provided personal security for agents of the criminal investigative services of all the military branches.</p>

			    <p>It was hard to return to his job as a carpenter. It didn’t match the adrenaline rush of war. He became an instructor with the National Guard. Being around other soldiers helped, because they understood.</p>

			    <p>Stowe heard about the Nest from his mom, who had visited more than a year earlier with her Blue Star Mothers of America group. She suggested he come and volunteer. Finally he did, in August 2012. He helped fix a couple of rooms, did some carpentry work. He got to know Butler and told her he’d like to come back.</p>

			    <p>After his return home, Stowe began struggling with PTSD again. He went back to the Nest and has been there off and on for more than a year.</p>

			    <p>Now he’s helping train a dog that he hopes will be the first service dog trained at the Nest. Butler’s vision includes opening the Freedom Dog House, which will provide training for service dogs.</p>

			    <p>“I like to tell people she just goes out and gets the money so we can keep the lights on and the heat going,” Stowe said. “It’s us — we’re the ones who do the cooking, we do the cleaning, shovel snow.”</p>

			    <p>Felix Sanchez spent five years in the Marines special forces and served four overseas tours, including one in Iraq and three in Afghanistan.</p>

			    <p>After he left the military in 2011, coming home was hard. The New York native drifted around the country, teaching snowboarding in Utah, chopping wood in Oregon, flipping burgers in Idaho and baling hay in Montana.</p>

			    <p>Finally, Sanchez ended up in Mankato, and that’s when problems began to arise. He began drinking heavily and got into legal problems.</p>

			    <p>Sanchez sought treatment at the St. Cloud VA Health Care System, where he underwent therapy for PTSD. There, he met another veteran who told him about the Eagle’s Healing Nest. At first, he was hesitant. It seemed too good to be true.</p>

			    <p>But at the Nest, he found camaraderie among the other veterans, who kid each other and work together to build a home. Sanchez finds peace in the barn working with the horses and other farm animals.</p>

			    <p>“It’s kind of re-instilling the whole band of brothers thing,” Sanchez said. “It’s really conducive to healthy growth here.”</p>

			    <p>He plans to stay until he completes his bachelor’s degree in computer science and takes care of his legal issues.</p>

			    <p>“I can see this place multiplying all over the country,” Sanchez said.</p>

			    <p>The Nest has attracted nationwide attention and has been heralded as a model for future veterans’ homes. It’s been featured in local and national media. Butler has met with politicians including U.S. Sen. Al Franken and testified in front of legislative committees at the state Capitol.</p>

			    <p>Butler’s vision is boundless. She intends to buy the Sauk Centre property and turn it over to a trust, which would be run by a hand-selected group of veterans. She also has plans to add a building for female veterans as well as the training center for service dogs.</p>

			    <p>Eventually, Butler hopes to replicate the Nest model until there is one in every state.</p>

			    <p>Not everyone has embraced the concept. Butler had hoped that Stearns County would approve a group residential housing contract, which would allow the Nest to receive funding to care for low-income veterans with disabilities who qualify.</p>

			    <p>Until recently, county officials recommended against it, publicly stating that the St. Cloud VA is able to provide all the necessary services for veterans. But in July, the county board voted to approve the contract.</p>

			    <p>Privately, some have expressed concerns about the number of veterans from all over the country with a variety of needs staying at an unlicensed facility. Butler hasn’t finished her college degree and isn’t a licensed psychologist. Some of the residents may not have official veteran status because they’ve been dishonorably discharged or court-martialed.</p>

			    <p>None of that has deterred Butler.</p>

			    <p>“I don’t like statistics. I don’t like politics. I don’t like any of those things,” Butler said. “I was told, ‘You’re not in the know, you don’t have the qualifications.’ I’m a mom, and I know how to keep my family together, and I know how to fight.”</p>

