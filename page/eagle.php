			    <p>“They keep picking each other up. They keep holding each other accountable,” Butler said. “They don’t leave anyone behind.”</p>

			    <p>Among those who have found shelter at the Nest is 39-year-old Air Force veteran Todd Kuikka, an expert at explosives disposal who served multiple tours in Iraq and Afghanistan.</p>

			    <p>After he was injured and medically discharged from the military, Kuikka had difficulty adjusting to civilian society.</p>

			    <p>“The military kind of hones you and hardens you to turn into a square peg,” he said.</p>

			    <p>Kuikka struggled with post-traumatic stress disorder that made him unable to be in crowds. He wanted to attend the residential PTSD treatment program at the St. Cloud VA Health Care System, but he said he was denied entrance.</p>

			    <p>After moving back to his native Minnesota and getting married, Kuikka attended St. Cloud State University. He was finishing his senior thesis and close to graduating when the stress got to him, causing him to isolate himself and spiral out of control. He considered suicide.</p>

			    <p>Last August, Kuikka moved into the Eagle’s Healing Nest. He stayed there for eight months until he felt stable enough to move back home with his wife, Erika, and their 14-month-old daughter Sisu. They recently moved into a new home through Operation Homefront, a Texas-based nonprofit that provides homes for wounded veterans.</p>

			    <p>Kuikka said the support and camaraderie at the Nest saved his life and helped him get back on his feet. He believes it should be a model for other programs around the country.</p>

			    <p>“If you don’t have veterans who faced some problems and found a way out, then you’re essentially setting up that next veteran to fail,” Kuikka said.</p>

