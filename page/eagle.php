			    <p>Butler called around to find him help, but the St. Cloud VA Health Care System told her they didn’t have space for him. She called the adjutant general of the National Guard, who helped find an opening for her son to be placed on a 72-hour psychiatric hold. By then, she was angry.</p>

			    <p>“It shouldn’t be a matter of who you know,” Butler said. “Those facilities are supposed to be there to help our veterans. ... These veterans served our country, and we are losing another generation.”</p>

			    <p>After her son came home from the hospital, he still wouldn’t talk. The medications he was prescribed were a quick fix, but not a way to live.</p>

			    <p>Butler reached out to a local doctor who was also a Vietnam veteran. He recommended giving her son what he needed: the soldiers with whom he had served.</p>

			    <p>So Butler did. Her home filled up with veterans in crisis. She was answering phones, going to visit families. There were too many to help.</p>

			    <p>The adjutant general called to check how her son was doing. Butler thanked him, but she also let him know how angry she was.</p>

			    <p>“I told him that we were going to lose another generation, that they deserve better than what Vietnam got,” she said. “And he said, ‘Don’t come to me with a problem. Come to me with a solution.’ ”</p>

