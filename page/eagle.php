			    <p>They call her Ma.</p>

			    <p>She’s part house mother, part counselor, part drill sergeant. She holds their hand in the middle of the night when nightmares won’t let them sleep. She answers her cellphone at any hour in case it’s a depressed veteran who needs to talk. She calls their bluffs and regularly threatens to use her size 9 shoe to straighten them up.</p>

			    <p>Melony Butler’s mission to help veterans started when she was a child, sitting around a bonfire with her stepfather and his Vietnam battle buddies. He made her promise she would never let anyone forget the sacrifices they’d made.</p>

			    <p>She married a military man who served two tours in the National Guard, one in Iraq and one in Afghanistan. Three of their four sons have served in the Guard. The oldest was deployed to Iraq, the middle son to Afghanistan.</p>

			    <p>Butler became an active volunteer at Camp Ripley near Little Falls and with the Beyond the Yellow Ribbon campaign. When her husband’s unit returned, she saw many military families struggling and falling apart.</p>

			    <p>After the oldest son returned from Iraq, he was depressed, his marriage in trouble. He called her one night to say goodbye. Frantic, she kept him on the phone and woke up his dad and brother. She kept talking to him until they found him and brought him home.</p>

			    <div class="large-4 medium-4 column right panel" style="margin-left:10px;">
			      <a href="page.php?page=videobutler"><img src="assets/img/p3/eagle/eagle1vid.jpg"></a>
			      <h5>VIDEO: <b>Melony Butler talks Nov. 22 about the path that led her to start the Eagle's Healing Nest in Sauk Centre. She wanted to create a place to help returning veterans who were falling through what she saw as cracks in the established system.</b></h5>
