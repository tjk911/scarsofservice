		<p>While the federal government has increased its research and public education efforts related to TBIs in recent years, many say not enough is being done.</p>

		<p>Even mild or moderate TBIs can cause a range of problems, including headaches, sleep disturbance, balance problems, fatigue, concentration and attention problems, memory gaps, anxiety, depression and mood swings.</p>

		<p>In recent years, TBIs have been in the news because of concern over repetitive head injuries in sports and their long-term effect on the brain.</p>

		<p>In the military, TBIs are most often caused by blasts, bullets, shrapnel, falls, vehicle crashes, sports or assaults. Among deployed service members, blasts are the leading cause of TBIs.</p>

		<p>TBIs are classified as mild, moderate, severe or penetrating. The vast majority of TBIs are mild, also known as a concussion.</p>

