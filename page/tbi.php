		<h4>St. Cloud VA puts focus on traumatic brain injuries</h4>
		<p>They have been have been called the signature wound of the wars in Iraq and Afghanistan.</p>

		<p>A traumatic brain injury is a blow or jolt to the head that disrupts the normal function of the brain and can have long-term cognitive effects. Because of advances in medical care, more service members are surviving their injuries but returning from combat with TBIs.</p>

		<p>According to the Defense and Veterans Brain Injury Center, 300,707 U.S. service members have sustained a traumatic brain injury from 2000 through March 2014. That includes both deployed and nondeployed troops.</p>

		<div class="large-12 columns hide-for-small">
