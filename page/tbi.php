		<h4>Greater focus in VA</h4>

		<div class="large-4 medium-4 column right panel" style="margin-left:10px;">
		  <img src="assets/img/p1/TBI/tbimug.jpg">
		  <h5><b>Heidi Ampe, licensed independent clinical social worker and special patient populations program manager in the rehab medicine department at the St. Cloud VA Health Care System.</b></h5>
		  <h5 class="caption">(Photo: Kimm Anderson, kanderson@stcloudtimes.com)</h5>
		</div>

		<p>Heidi Ampe is the program coordinator of the polytrauma support, vision impairment services and spinal cord injury teams at the St. Cloud VA Health Care System. She started in the new position about six years ago.</p>

		<p>That’s when the VA began focusing more on treating veterans who were struggling with the effects of traumatic brain injuries for which they might never have been treated.</p>

		<p>When screening veterans for the first time, VA staffers ask anyone who served in Iraq or Afghanistan whether they were exposed to a blast or other head injuries that might have caused a concussion, which is the same as a mild traumatic brain injury, Ampe said.</p>

		<p>If the answer is yes, the veteran is asked what the conditions were and what it felt like. Did they lose consciousness? Feel dazed or disoriented afterward? Do they have trouble remembering what happened just before and after the event?</p>

		<p>Those are all indicators that suggest brain function was interrupted, Ampe said. But figuring out the effect of such an injury can be difficult, she said.</p>

		<p>While there has been a lot of new focus on concussive injuries in sports, a combat-related TBI can be totally different, Ampe said. In sports, athletes are usually wearing protective gear in a game with rules and a support system surrounding them, she said. After an injury, they are usually taken off the field and assessed.</p>

		<p>“On the football field, you get your bell rung, you know what’s happened,” Ampe said. “And the effects of it later, you can be pretty sure are related to that.”</p>

		<div class="large-8 medium-12 column right panel" style="margin-left:10px;">
		  <img src="assets/img/graphics/tbi.jpg" style="width:100%">
		</div>

		<p>With a service-related traumatic brain injury, it’s not as clear. A veteran might be suffering from sleep deprivation, but that could be related to the psychological trauma of surviving a blast in a war zone, Ampe said. Often, such situations are chaotic, and the veteran might not have received a medical evaluation at the time, she said.</p>

		<p>In some cases, the injury didn’t occur during combat. Maybe the veteran is a mechanic and a tool fell on his or her head, Ampe said.</p>

		<p>The St. Cloud VA rarely treats anyone with moderate or severe brain injuries. Those veterans are usually treated in Minneapolis. At St. Cloud, the veterans typically have not been previously assessed for a TBI. The injury might have happened months or even years ago.</p>

		<p>TBI-related difficulties with memory, concentration, organization and decision-making affect a lot of areas of life, Ampe said. “Whether they’re going back to work or school, whether they’ve got a family that they’re taking care of at home, or they just need to keep track of their VA appointments — whatever they have going on.”</p>

		<p>The VA’s goal is to help the veterans adapt and improve their quality of life, Ampe said. Part of that involves looking at the whole patient and treating any problems they might be struggling with, such as mental health distress, substance abuse or sleep problems.</p>

		<p>“Maybe they haven’t gotten a lot of structure back in their life,” Ampe said. “If they don’t have kind of a routine to how they live their day or their week, then it can feel kind of chaotic.”</p>

		<p>In some cases, symptoms that might seem minor, such as difficulty concentrating, become a problem after a veteran goes back to work or college. Suddenly, he is having difficulty keeping up with schoolwork or other tasks, Ampe said.</p>

		<p>“That thing that maybe seemed mild in isolation is having this kind of snowball effect down the road,” she said. “We don’t want that to happen. We want to really have them in the best position to be successful.”</p>

		<h4>Medical findings</h4>

		<p>Medical research on the effects of traumatic brain injury has come a long way since the early days of the wars in Iraq and Afghanistan.</p>

		<p>Years ago, the belief was that if you have a brain injury, it will always remain injured, Ampe said. But now, doctors and scientists understand the brain can heal, she said.</p>

		<p>“Sometimes people think of it as a chronic condition, and there can be lasting effects,” Ampe said. “I guess I would rather think of it as an injury, which it is. By comparison, if I have a broken bone, I will expect that it will heal. With good treatment, it will heal properly. It’s never going to be exactly like it used to be, but I won’t always have a broken bone.”</p>

		<p>VA staff now understand that a traumatic brain injury can change the way the eyes work together, Ampe said. Optometrists now do eye exams to look for those changes and provide vision therapy to help correct problems, she said.</p>

		<p>The VA tries to help veterans find practical ways of coping with the effects of a TBI. They might learn how to use the alarm on a smartphone to remind themselves to pick up the kids from school or pay their insurance. Or if they get distracted while driving, they might use a GPS system to let them know when to turn.</p>

		<p>“A lot of times it’s using what a person has in their pocket right now,” she said.</p>

		<p>Sometimes, those small adjustments can go a long way toward improving a veteran’s quality of life, Ampe said.</p>

		<p>“When you have some strategies that help you function better in your everyday life, then what happens to that anxiety? That comes down, too, because you know you’ve got some ways to do this.”</p>

		<p>It’s not always easy to get veterans the help they need, however. Often, when they return home from deployment, enrolling in the VA for health care is one of the last things on their mind.</p>

		<p>“Some people are saying, ‘You know what? Just get me home. I’ll deal with all that later,’ ” Ampe said.</p>

		<p>Her advice to veterans is don’t try to tough it out on your own.</p>

		<p>“There’s things you can do,” Ampe said. “And it’s not just to help yourself, but to help your family and ... people around you.”</p>

