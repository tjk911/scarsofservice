			<p><i><b>They’re turning people down that really, really need it, because a couple of college-educated shrinks that read a bunch of books and talk to us maybe every now and then deem us not ready for the track. So they send you out the door with a bag of tools that they know are going to fail us, and we’re going to go back to self-medicating, drinking heavily. They set us up for failure.</b></i></p>

			<p>Without the structure of the military, Klutenkamper spiraled downward. He applied for a security job at a casino, but he was turned down because he was deemed too aggressive.</p>

			<p>On the application, when asked what he’d do if he saw a man beating a woman up, he’d responded honestly. He’d beat the guy up. The correct answer was call for help, but Klutenkamper couldn’t imagine standing by and watching.</p>

			<p>He got a job at a gas station but lasted a week. He couldn’t stand being instructed in detail how to perform simple tasks, or working with teenagers who seemed to think they were entitled to things without having to work for them.</p>

			<p>He saw people smoking while they pumped gas, and couldn’t understand what they were thinking. If they flicked the cigarette on the ground near a puddle of spilled gas, and the fumes ignited, the whole place could blow up. He’d seen that enough times already. He lost patience and yelled at them over the speaker.</p>

