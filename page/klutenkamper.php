			<p><i><b>Here, it’s not just three meals and a place to sleep. It’s not just shelter. I’m back among some combat veterans. ... It’s the brotherhood again. I’m not paranoid, because I know I can trust these guys. ... I don’t feel as if I have to be 100 percent responsible for the safety and well-being of everybody else around me. Like I know everybody has everybody else’s back.</b></i></p>

			<p>Klutenkamper still hopes to go to college and major in social services, with an emphasis on treating post-traumatic stress disorder and chemical dependency. He wants to help other veterans, to pay it forward. But he’s worried about how he will handle the stress and workload of college.</p>

			<p>In the meantime, he’s acquired a goofy, half-grown golden Lab named Odie, who’s being trained to be his service dog. Watching Odie romp and play distracts Klutenkamper and occupies his constantly vigilant brain. He laughs more easily.</p>

			<p><i><b>Here it’s just nice, because you can relax and you can decompress, and you can do it at a rate where the only person putting pressure on yourself is you. ... This place has been a lifesaver. It’s given me a task and a purpose again.</b></i></p>

			<p>For now, Klutenkamper will stay at the Nest.</p>

