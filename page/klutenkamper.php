			<p>They were sometimes referred to as “bullet sponges.” The job of the Army infantry in the war zones of Iraq and Afghanistan was to be the front line.</p>

			<p>When Daniel Klutenkamper joined the Army in 2003, he knew there was a good chance he’d be sent to a war zone. He was OK with that. By October, the Missouri native who grew up in Lakeville was on his first overseas deployment to South Korea. In 2004, he was sent to Iraq.</p>

			<p>His unit was stationed between Fallujah and Ramadi. After a few months, they moved to Ramadi itself, the site of some of the heaviest fighting of the Iraq War.</p>

			<p>When he returned home, the adjustment didn’t go well.</p>

			<p><i><b>When we first got back from our first tour, we were pretty much told if anything you saw or did overseas bothered you that you were to (expletive) delete it. And if you sought any sort of mental or behavioral help, you would be barred from promotions and eventually, you would be chaptered out of the service. Because that wasn’t how we were supposed to be.</b></i></p>

			<p><i><b>So a lot of us just started drinking heavily. It was one of the only ways I could fall asleep was hit the bottle pretty hard every night.</b></i></p>

			<p>Klutenkamper got into some legal trouble and sobered up for a while. Then came the orders for a second deployment. He started drinking again, knowing the fighting was likely to be more intense than during the first tour.</p>

