			<p>In 2006, he was sent back to Iraq for a yearlong tour that would stretch into 15 months due to the surge. In December 2007, he came home.</p>

			<p>But then in May 2009, he was sent to Afghanistan for a year, to Kunar Province. Afghanistan made the two tours in Iraq look like a training exercise. The enemy knew how to fight, and they blended into the civilian society.</p>

			<p><i><b>It was always expect the worst, and there really was no hope for the best. Because regardless, even if it wasn’t your friends getting killed, somebody else was being shot and killed. ...</b></i></p>

			<p><i><b>You think you can go through all that stuff and not have any of it bother you. If none of it bothered you, there is something wrong with you. The fact that it does bother you means that you have a lot of humanity left.</b></i></p>

			<p>After Klutenkamper returned to the United States, he turned down an offer to re-enlist. He didn’t want to get out of the Army, but re-enlisting would have meant he’d be back in Afghanistan within six months. He needed time to decompress.</p>

			<p>Klutenkamper sought mental health help before he left the Army, but found it lacking.</p>

			<p><i><b>We were actually told by the higher-ups that if anything bothers you, you’re a pussy. We don’t have pussies in the infantry. We have hard-core, door-kicking bastards that will kill anything and everything that they need to. So that was the mentality we lived with for so damn long.</b></i></p>

			<p><i><b>All that bloodshed sitting on your hands just wears you down, but you don’t want to say that it bothers you.</b></i></p>

			<p>Veterans who did receive treatment were often prescribed medications, which Klutenkamper didn’t want. He’d seen veterans who were prescribed opiates, such as oxycodone for pain, become addicted. When their prescriptions ran out, they were on the streets looking for whatever they could get.</p>

			<p><i><b>Instead of actually fixing the issue, they just gave you pills for stuff. I didn’t want pills. I just wanted ways to go about sorting things in my head that would be constructive, instead of getting a pill that would eventually get you addicted to it.</b></i></p>

			<p>Klutenkamper was told when he left the military that his records would be transferred to the Veterans Administration. But it took him a year to get his first appointment at the Minneapolis VA. In the meantime, he self-medicated with alcohol and marijuana.</p>

			<p><i><b>My binge drinking almost killed me a couple of times. So I switched from drinking and went to pot, which I figured was the lesser of two evils. ... I know eventually, I would have been caught with it and ended up in jail.</b></i></p>

			<p><i><b>That’s why a lot of us would drink and we would use, because we didn’t want to face the demons that had been following us for years and years and years. So instead of facing those demons, we would try to kill them through use, without regard to what our use was doing to ourselves or our families or the ones we held close to us. We just didn’t see it.</b></i></p>

			<p>He received treatment at the St. Cloud VA Health Care System for alcohol and substance abuse, and underwent cognitive processing therapy at the Minneapolis VA. It helped, but after 12 weeks, it was over.</p>

			<p>He was supposed to be fixed, and he wasn’t.</p>

			<p>He’d hoped to get into the specialty track for post-traumatic stress disorder at the St. Cloud VA, but he learned he didn’t get into the program.</p>

