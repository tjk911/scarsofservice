			<p>Klutenkamper’s options were limited. He had eight years of infantry experience, but it didn’t train him for many civilian jobs except law enforcement. With only a high school diploma, most careers were out of reach.</p>

			<p>He struggled with being around people, especially crowds. His friends asked him to go the movies, but he refused. A dark, crowded theater filled with strangers and only one exit? A mass casualty situation, for sure.</p>

			<p>His friends joked and called him paranoid. But it was the life-and-death way he’d been trained to think.</p>

			<p><i><b>I feel more comfortable overseas in Iraq and Afghanistan than I do walking around the streets here, and I don’t know why. ... I’d just almost rather be over there than here sometimes, because life made sense over there.</b></i></p>

			<p>He thought about going back to school, but the idea of being on a campus where weapons are banned makes him uncomfortable. And at 32, he doesn’t relate to college students anymore.</p>

			<p>Then he heard about a place in Central Minnesota where veterans were welcome to stay and heal together. He decided to give it a try.</p>

			<p>Klutenkamper arrived at the Eagle’s Healing Nest in Sauk Centre last October. It has become his sanctuary, a place where he finally feels safe.</p>

			<p>He talks to the older veterans, the ones who served in Vietnam and Korea. They share common stories and offer advice. He listens.</p>

