			<p><i><b>There’s a few gentlemen here that I know if it wasn’t for this place, they would drink themselves to death or go to a different means to ending it. I’m one of them. This place has literally saved my life, because I was so tired of not understanding that I wasn’t crazy.</b></i></p>

