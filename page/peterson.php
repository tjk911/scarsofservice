			<p>He recently was declared 100 percent disabled, and she recently was accepted into the family caregiver program. That acknowledges that she has to care for Brian and brings in a $588 monthly stipend in addition to the $3,300 he gets from being disabled.</p>

			<p>It also makes her eligible for mental health treatment at the VA.</p>

			<p>Brian recently filed for bankruptcy protection to get out from under the more than $200,000 in medical bills from his surgery. It gets their family back on better footing, but at a cost. They had to give up their two newer vehicles for older ones. Christian was forced to give back her wedding ring as well.</p>

			<p>“I cried a lot. I’m OK with all of that now,” she said. “The debt was something that we didn’t pay for because we had to put food on the table for the kids.”</p>

			<p>She’s also trying to find a way to explain to those kids — 4-year-old Jessi and 2-year-old Andi — what has happened to Brian and why he sometimes loses his temper with them.</p>

			<p>She has joined Facebook groups, most of them military-related, about PTSD. She can read about how others relate to their spouses and their responses so she has an idea how to respond.</p>

			<p>She credits the book “Tears of a Warrior” with helping her understand what was going on with Brian’s PTSD. But nobody has answers about how to explain to the kids why Dad all of a sudden gets mad for no reason and starts raising his voice, she said.</p>

			<p>“No one has told me how, and I haven’t found or read anything, on how to do that,” Christian said. “How do you explain PTSD to a 4-year-old?”</p>

			<p>“It hurts so bad,” Brian says. “To do that stuff like that to your own kids, you know, how could you ever do it again? But you just can’t control it.”</p>

			<p>With all of his physical problems and mental health challenges, Brian would do it again — volunteer to go overseas and fight, knowing how he has turned out.</p>

			<p>“In a heartbeat,” he said.</p>

			<p>“It’s who I am. Serve my country. There’s a lot of people in our country that don’t either have the heart to do it or, — well, I shouldn’t say that. They have the heart to do it but they don’t have the body to do it or the mental capacity to do it. So, we need people like me to do it so we can continue to have a country like we have.”</p>

			<p>Even if it means he returns without the body or the mental capacity he had when he left.</p>


