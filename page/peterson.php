			<p><i><b>“Brian: I’m sorry that you had to see everything that you did overseas. I sorry you had to see all those dead people and not be able to help anyone when you could of. Please remember you always did your best and kept your team alive! You took control when you had to and did your job to the highest honor. Be proud.”</b></i></p>

			<p>“Healthy self-talk” was a cornerstone of the program, Brian said — remembering to say positive things when you catch yourself with unhealthy thoughts.</p>

			<p>He learned how any type of abuse — drugs, alcohol, sexual — interacts with PTSD. He learned about depression and how positive thoughts and reinforcing talk can bring back self-esteem.</p>

			<p>He learned about the things that can trigger explosive episodes like those ended with him cursing at Christian or their daughters.</p>

			<p>“It was really effective,” Christian said of the dual-diagnosis program. “There are some things that they are just never going to be able to fix, not that there are things that are wrong with him. He’s just never going to be his pre-deployment self again. That’s never going to change.”</p>

			<p>Christian used to take great offense when he cursed at her, called her a bitch.</p>

			<p>“I would get really mad. I mean, it still hurts when he says it, but ... I have to understand that, you know, some things I just have to live with,” she said.</p>

			<p>A greater understanding of what causes his problems is only one step. The learning curve about his PTSD is continual.</p>

			<p>Sometimes he doesn’t know what it is that makes him erupt, but he’s gained a better understanding of the things that amp up his stress level.</p>

			<p>“What’s it like walking around not knowing if you’re going to freak out in the next second?” he asks. “It sucks. It’s scary.”</p>

			<p>He and Christian have talked about it, and she’s referred to him as a walking time bomb.</p>

			<p>“In a way, no I’m not a walking, ticking time bomb, but I can tell, especially with actually going through treatment at the VA, I can tell throughout the day when I’m getting more and more stressed, more and more anxiety, more and more irritated and I can tell when my fuse is getting a lot shorter,” he said. “And the shorter and shorter my fuse gets the less it’s going to take to blow up.”</p>

			<p>And there are still blowups. It could be the prolonged crying of one of the kids or something as simple as one of the kids taking the dog outside when he didn’t want to dog to leave the house.</p>

