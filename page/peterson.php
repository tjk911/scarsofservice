			<p>Sometimes he can remember after it’s done what it was that set him off. Sometimes it’s just a fog and he can’t recall what precipitated it. And sometimes he disassociates during the episode.</p>

			<p>“We’ve learned that her and I have to discuss it right away,” Brian said. “That was a hard thing to learn. That caused a lot of fights just so we could get to the point where we learned that right away, when it happens, we both need to calm down and then come at each other both calm, relaxed, collected. Come at each other like referees, kind of, and apologize and say, ‘I did this wrong, we did this wrong and next time we should try this.’”</p>

			<p>Christian has had to quit a paid internship in her field because Brian can’t be home alone for long without getting anxious, irritated or even suicidal. So she only leaves the house in the morning for a workout and then returns to care for Brian.</p>

			<p>“For the family and for my sanity and for the kids and for him, I had to stay home,” she said.</p>

			<p>Brian can’t do the family’s financials, often won’t eat if he isn’t reminded and can’t take care of the kids alone for an extended period.</p>

			<p>He’s aware of the effect that his disability has on her life.</p>

			<p>“I feel even more horrible because she spent all this time in school and she had a job that she really loved and it’s hard on me knowing that I’m the reason she can’t go to work,” he said.</p>

