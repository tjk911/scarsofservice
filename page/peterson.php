			<p>He could feel the vibrating in his head. His organs seemed to shake.</p>

			<p>“My eyeballs were bouncing in my skull.”</p>

			<p>That concussion came after a previous IED explosion knocked him unconscious and sent him into a three-day “concussion coma,” as he calls it.</p>

			<p>The traumatic brain injury was just one of the wounds that Peterson brought home to St. Cloud. Shrapnel injuries to his wrist, blown discs in his back that needed to be fused together, discs in his neck that eventually will need to be fused, a shoulder that repeatedly popped out of its socket have left the 30-year-old with a body that seems at least twice its age.</p>

			<p>“It sounds like popcorn when I move,” he tells his wife, Christian. “I am broken, destroyed.”</p>

			<p>He can’t walk the stairs without leaning heavily on the railing. He can’t sit for more than a few minutes without his back tightening up, a result of the fused discs. He sometimes passes blood when he urinates. And the post-traumatic stress, which he’d masked for years with painkillers, reared up six years after his service ended, leading to a suicide attempt in July 2013.</p>

			<p>He’s not unlike a whole generation of soldiers. They volunteered to fight and have come home with debilitating injuries that, in some cases, are eating away at them silently.</p>

			<p>Learning what it is that is gnawing at you — both physically and mentally — is one thing. Understanding it is another.</p>

