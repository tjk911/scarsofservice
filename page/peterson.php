			<p>Specialist E4 Brian Peterson wanted badly to serve his country, joining the National Guard at age 17 after his junior year in high school. He completed basic training during the summer before his senior year.</p>

			<p>Right after high school, he did his advanced individual training, learning the job of mechanic. It wasn’t what he dreamed of doing, but it was the quickest way to reach his goal of being deployed.</p>

			<p>“That’s the one thing that I’ve always wanted and dreamed of, was to go serve my country,” he said.</p>

			<p>It finally happened in 2004, when he was sent to Kuwait.</p>

			<p>It was there he was blown off a truck by a gust from a sandstorm and landed on his shoulder, dislocating it. From then on, it would pop out of joint routinely and he would need help getting his body armor off to reset the shoulder.</p>

			<p>His second deployment came two months after the first ended. He was in Iraq from early 2006 to July 2007. His unit was a mishmash of medics, cooks, mechanics.</p>

			<p>Their scout vehicle would clear the way for the convoy and put down flares to illuminate the way. It was a rewarding and dangerous job, and he talks with pride about his unit coming home without casualties.</p>

			<p>There were close calls, though. He saw a comrade nearly die from a roadside bomb that left his body ripped by shrapnel and the road soaked in blood.</p>

			<p>Brian thinks the discs in his back were injured from a collision between the Humvee and a dump truck. He counts at least three concussions from explosions targeting his unit.</p>

			<p>The physical injuries that were beginning to pile up were mostly treatable, but bigger demons were lurking inside.</p>

			<p>Brian returned from his second deployment already looking ahead to a third. When his unit gathered in Cottage Grove to prepare for another deployment, he met Christian Gilman, a medic in the unit to which Brian was attached as a mechanic. They both were prepared to deploy when they reported to Fort Ripley.</p>

			<p>That’s where Brian learned that he wasn’t going to lie his way into a third deployment.</p>

			<p>They stopped him from going on the third tour.</p>

			<p>“They said I was medically unstable,” Brian says with a laugh.</p>

			<p>He told his doctors that his back was just fine, along with the rest of his body. The problem was that the first sergeant and commanding officer knew him from his previous tours.</p>

			<p>And Christian soon would find out that she wasn’t going to be deployed either.</p>

			<p>She was pregnant with her first child.</p>

			<div class="large-12 medium-12 column panel">
