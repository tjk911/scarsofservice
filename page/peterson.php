			<p>So the two began to carve out their life in St. Cloud, Brian taking a job at Sears as a mechanic and Christian going to St. Cloud State University to study public relations.</p>

			<p>Brian went through some outpatient treatment for post-traumatic stress and endured the continuing pain and degradation of his back.</p>

			<p>“I’d go to the bathroom and stand at the toilet and nothing would work anymore,” he said. “I’d just stand there and I couldn’t go. I’d try to walk and my left foot was dragging. Couldn’t lift my left foot up anymore.”</p>

			<p>He turned to painkillers to numb the throbbing, and while the narcotics masked the pain in his back, they also masked his post-traumatic stress symptoms.</p>

			<p>The job at Sears lasted only about eight months before he started blowing up on employees, customers and his boss.</p>

			<p>He quit that job and “pretty much sat at home drinking and popping pain meds,” he said.</p>

			<p>His back problems continued to worsen and so did his reliance on painkillers. He battled with the VA — and lost — about whether his back injury was service-related.</p>

			<p>The VA wouldn’t order an MRI, he said.</p>

			<p>The doctor thought the problem was Brian’s kidneys and sent him to the urologist, who found no problems.</p>

			<p>He finally got his civilian doctor to order the MRI and a discogram, which showed three discs destroyed and a fourth herniated.</p>

			<p>His VA doctors argued that his injuries weren’t connected to his service. Brian spent the better part of six years using cortisone shots, painkillers and physical therapy while trying to get the surgery he needed. His civilian insurer approved the surgery then denied the claim afterward, saying his back injury was a pre-existing condition. He finally had the operation in June 2013.</p>

			<p>Although one physical injury was getting addressed, his mental health wasn’t.</p>

			<p>Then the medical bills started to come in.</p>

			<p>Brian already was battling diagnoses of traumatic brain injury, post-traumatic stress, intermittent explosive disorder, borderline personality disorder and depression. The seemingly smallest things would set him off, and he often didn’t know why.</p>

			<p>The same day Brian had his back surgery, Christian learned she had a brain tumor. The death of her brother-in-law would soon follow.</p>

			<p>“It was a really stressful time,” Christian said. “And to add all of that on top of it with his PTSD and the mix of the narcotics, he just decided that he was better off dead and that I should take the life insurance money and just be fine.”</p>

			<p>Military life insurance pays off for suicide, unlike civilian insurance.</p>

			<p>So on July 10, 2013, Brian decided Christian would be better off without him around.</p>

			<p>They had been at a Summertime by George! concert at Lake George when Brian went to sit down in a lawn chair that belonged to Christian’s sister.</p>

			<p>The chair collapsed underneath him. Someone told him that he was lucky he hadn’t broken a chair that belonged to another family member.</p>

			<p>Brian blew up, upset that people seemed to care more about the chair than whether his surgically repaired back was OK from the fall.</p>

			<p>He stormed off and went home alone.</p>

			<p>“I grabbed the bottle of Percocet and took a whole handful of those and some Oxycontin and grabbed some beer and just sat there and started drinking beer,” Brian said. “By that time something clicked in my head that ‘Yeah, you’re probably done now, so say goodbye.’ ”</p>

			<p>“He wrote me a nice little letter,” Christian said.</p>

			<p>She found the goodbye note after rushing home when she sensed something wasn’t right. She found him “stoned out of his mind,” she said, and rushed him to the hospital.</p>

			<p>He hadn’t taken quite enough to kill himself.</p>

