			<p>The next day, Brian was in a double-locked unit at the VA. He was discharged the day after that to the dual-diagnosis program, which addresses a veteran’s PTSD and substance abuse issues together.</p>

			<p>But he sat there for four days without seeing anyone prepared to counsel him. He recalls staff playing the movie “Saving Private Ryan” while he waited to see a doctor.</p>

			<p>“I was in the program for four days and didn’t see one doctor,” he said. “I’m at the lowest of my low. I just attempted to commit suicide and you’re not going to have me see a doctor or anything? And I was like, I’m out of here.”</p>

			<p>When he left the VA, it was the first time in five or six years that he had been off of narcotics for five straight days, and Brian began to feel like he was going to be able to handle life. I can do this, he told himself.</p>

			<p>I can do this on my own.</p>

			<p>He came home and went back to his job designing and selling refrigeration systems.</p>

			<p>But the flashbacks, the nightmares, the anxiety came crashing down on him within days.</p>

			<p>“My hands would be trembling at my keyboard and I’d be looking behind me constantly. People would walk by and I would be freaking out. I couldn’t wait to get home. I’d be driving home and the whole time it was like I was on a convoy traveling to Baghdad again. Every vehicle on the road I’d be thinking it was (a vehicle with a bomb), thinking that it’s going to blow up on me. I just couldn’t take it anymore.”</p>

			<p>He made it three days at work and two weeks out of the program before he woke Christian up in the middle of the night after he’d stayed up late drinking.</p>

			<p>He can’t remember how many drinks he’d had, but he recalls stopping himself at midnight.</p>

			<p>“I woke her up and I was like ‘I don’t want to do this again. I have to go back,’” he said.</p>

			<p>Christian could see it in his eyes.</p>

			<p>“He was terrified,” she said.</p>

			<p>He went back to the dual-diagnosis program and this time he vowed to stay the 45 days it required. They pressed the VA to get him in right away, without the wait he had experienced before.</p>

			<p>This time it worked.</p>

			<p>One requirement of the program was that he write a positive message to himself about his service.</p>

			<div class="large-12 medium-12 column panel">
			  <img src="assets/img/p2/peterson/peterson2.jpg">
			  <h5><b>Peterson wrote this letter as a way of practicing compassion toward himself in the VA's dual diagnosis program shortly after a suicide attempt.</b></h5>
