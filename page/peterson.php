			<p>The scout gunner peered through the turret of the Humvee, surrounded by a sea of sand and darkness. Lights off to avoid detection, the truck crawled through the night, a convoy trailing about a mile behind.</p>

			<p>The job of that Humvee, and the soldiers inside, was to clear a safe passage for the convoy behind. Posting at the turret was Brian Peterson’s side job when he served in Iraq with the Red Bulls. It was a job of daily danger, an assignment to get shot at, blown up, taken out.</p>

			<p>“We were the truck to be eliminated so the rest of the convoy could go on safely,” Peterson said. “We were the ones to die, so the rest of the convoy could go. That was our job.”</p>

			<p>Never mind that Peterson’s official job title in the Army National Guard was mechanic. That’s just something he did when he wasn’t manning that turret.</p>

			<p>The third time, his unit got hit was by a 155 mm mortar round that blew up 6 feet from the Humvee. Caught between bombings from the north and the south and with all of the vehicle’s tires blown out, his unit had to save itself.</p>

			<p>It was a blast that rocked Peterson’s body and marked at least the third time his brain was concussed while serving his country in battle.</p>

