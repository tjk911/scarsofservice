			<p>So Tony waited until he turned 18, then enlisted himself. Four months later, he left for basic training.</p>

			<p>It was 2000, and the National Guard’s mission was much different than today: a minimal presence in the Gulf War, peacekeeping missions such as Kosovo and Bosnia. They were weekend warriors — one weekend a month, two-week drills in the summer. Deployments meant taking care of floods, fighting wildfires and cleaning up after tornadoes.</p>

			<p>That all changed on Sept. 11, 2001.</p>

			<p>Tony never wanted to go to war. He was happy in his own small world. But if the call came, he would go. It was his responsibility to fulfill his end of the contract.</p>

			<p>Tony was a maintenance mechanic in Company B, 434th Main Support Battalion. He was attached to a transportation unit that was put on active duty June 1, 2004.</p>

			<p>Three days later, they left for Fort Dix, New Jersey On Jan. 3, 2005, he was headed to Iraq.</p>

			<p>After landing in the Gulf region, they drove up into Iraq on a 24-hour convoy. Tony marveled at his surroundings — herds of camels and goats, mud homes alongside the road, people of different cultures.</p>

			<p>At Forward Operating Base Speicher, near Tikrit in northern Iraq, Tony was assigned as a night shift mechanic and became a Humvee specialist.</p>

			<p>Early on, the work wasn’t bad. The mechanics had little contact with the fighting outside the base. Sometimes trucks would come back with shrapnel or flattened tires, but nothing too serious.</p>

			<p>As time went on, a few more trucks came in with heavy damage. Some had been hit by improvised explosive devices and burned to the ground.</p>

			<p>Those rattled him more.</p>

			<p>But aside from a four-day rest and recuperation pass into northern Iraq and a two-week leave home, Tony rarely left the base. Occasionally, he was needed to ride along on a convoy to provide maintenance support and recovery.</p>

			<p>Any time they drove outside the camp, there was a sense of impending danger. Around every rock could be an IED, or someone waiting to kill you with an AK-47 or a rocket-propelled grenade.</p>

			<p><i><b>I don’t know if there’s a point that you get over it, or you become so in tune with it that it’s just a part of you. But I know for myself, the seven convoys I was on, it was there every time. It does make you a little bit more aware of what’s going on. You’re looking everywhere, trying to make sure your area is covered.</b></i></p>

