			<p>When Tony is wearing his prosthesis, his physical injury is nearly impossible to detect. So too are the invisible wounds he suffered, the mental struggles that some days make it difficult to even get out of bed in the morning.</p>

			<p>The reaction of others runs the gamut from gratitude to judgment. Curious children will follow him around a store, asking honest questions until they are shushed by embarrassed parents. Some people approach him, shake his hand, thank him for his service. Often Tony and Desiree finish eating in a restaurant and learn that their bill was paid by a stranger.</p>

			<p>Some comments aren’t meant to offend, but still wound. At least you came back alive. You’ve adjusted so well. Good thing it’s just your leg that’s missing.</p>

			<p>In many ways, life has gotten better, but not always easier.</p>

			<p>Tony and Desiree got married in August 2010. At times, their marriage has been happy. At times, it’s been strained nearly to the point of breaking.</p>

			<p>Tony’s depression and anxiety comes and goes. With mental health treatment, he’s learned to recognize the certain times of year that set him off: his “alive date” when the explosion occurred, the anniversary of his amputation, the date when a member of his unit was killed.</p>

			<p>During those times, Tony pulls himself inside his shell, overwhelmed with feelings of self-doubt.</p>

			<p><i><b>I have a tendency to focus on failures versus successes. And everybody sees the flip side of that. They see what I’ve accomplished, not what I’ve failed. And a lot of people don’t understand why I might complain about something, and yet, ‘Well, we’ve seen you cutting a tree off the house.’ Well, yeah, but you don’t see that I ended up off my leg for a week and a half afterward because I pushed myself way too hard to get it done, instead of just sitting back and going, ‘OK, I need help.’</b></i></p>

			<p>He knows the danger of self-loathing, the pitfalls of depression. He can recite the statistics: An estimated 22 veterans die of suicide every day in the United States.</p>

			<p>And yet there are days when the thought creeps into his mind and refuses to let go.</p>

			<p><i><b>One (suicide) is too many. But yet at the same time, I’m very understanding of where these soldiers are coming from in dealing with the demons in your head. ... I have a few of my comrades that I deployed with who have committed suicide since they came back.</b></i></p>

			<p><i><b>It’s not easy, and I fight every day to not be another one.</b></i></p>

			<div class="large-12 medium-12 column panel">
			  <img src="assets/img/p1/larson/larson4.jpg">
			  <h5><b>Tony Larson and his wife, Desiree, and son Jonathan leave the St. Cloud VA Health Care System after appointments in January. Larson was injured in an IED explosion that eventually claimed his right leg below the knee.</b></h5>
