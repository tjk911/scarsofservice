			<p>In October 2005, Tony’s unit was just a few weeks away from coming home.</p>

			<p>They were assigned to handle a transport to the Abu Ghraib prison, then head to Camp Victory in Baghdad to spend the night.</p>

			<p>Typically, whoever got to the truck first would drive the first leg, then they’d switch places for the return trip. Tony happened to reach the truck first.</p>

			<p>The trip was uneventful. Tony drove to the prison and Camp Victory, where they spent the night. The next morning they headed back toward FOB Speicher, Tony in the passenger seat.</p>

			<p>Outside of Camp Anaconda near Balad, the first gun truck in the convoy reported there was an IED on the right side of the road. As each truck passed by, the message was conveyed, “IED clear.”</p>

			<p>As Tony’s truck rolled up, he held the radio receiver in his hand. He looked at the device, and knew.</p>

			<p>He turned his head to the side.</p>

			<p>He didn’t even get out the words across the radio that they’d been hit. The truck filled with smoke and dust. His ears were ringing. He tried calling out to his driver, but there was no answer.</p>

			<p>Just a few months before, a member of Tony’s unit, Mike Mills, had been badly burned in a fiery crash. That image flashed into Tony’s mind. I’m not going to sit in this truck and burn.</p>

			<p>He opened the door and jumped out, as the truck was still rumbling along at 30 miles per hour. He tumbled like a broken hubcap.</p>

			<p>A gun truck pulled up behind the damaged vehicle, blocking the view of Tony to protect him. The other soldiers dragged him onto the road. Before the rest of the convoy could turn around to help, small-arms fire rained down from a nearby sand berm. They returned fire.</p>

			<p>Tony was wide awake. A piece of shrapnel had gone through his right foot and grazed his thigh. It felt like his foot was burning from the inside out.</p>

			<p>Triage efforts began. Tony had been trained as a first responder and a combat lifesaver. He tried treating himself for shock, asking himself questions. Where are you? What day is it? He tapped his toes to count them.</p>

			<p>Nobody would tell him anything about his condition. His driver had survived with just some shrapnel in his cheek. He knelt by Tony and told him if it hurts, squeeze my hand. Tony squeezed so hard the driver’s outer knuckles were touching. Two other soldiers had to take turns.</p>

			<p>When the medevac helicopter arrived, Tony tried to fight. If he went on a helicopter, that meant he was going home. He needed to get back to his unit and do his job.</p>

