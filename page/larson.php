			<p>His medications are in a locked dispenser that sounds an alarm when it’s time to take a dose and doesn’t stop until it’s shaken.</p>

			<p>Officially, Tony’s physical recovery is over. He has a collection of prosthetic feet, each a little different. One is made especially for cowboy boots. One hooks into a downhill ski. One he calls his dancing foot.</p>

			<p>Still, his body pays the toll. He’s had more surgeries. His other leg bears the brunt of his weight, and that knee is showing signs of wear and will likely need to be replaced.</p>

			<p>Tony became an advocate for America’s VetDogs, traveling with Tomme and speaking publicly about how having the dog has helped him. He’s been interviewed many times and appeared on national TV shows.</p>

			<p>In the difficult times, laughter helps them cope. Desiree kids Tony about having more feet than she has shoes. At Halloween parties, Tony wears a wooden peg leg as part of a pirate costume. He has a T-shirt that reads, “Soldier, 25 percent off.”</p>

			<p>The macabre sense of humor is a way to break the ice and relieve tension. They know it makes some people uncomfortable, but they don’t care. Laughter makes the unbearable a little easier.</p>

			<div class="large-12 medium-12 column panel">
			  <img src="assets/img/p1/larson/larson6.jpg">
			  <h5><b>Iraq veteran Tony Larson maintains his sense of humor, demonstrating how he sometimes uses his leg stump to create the impression of a moose. His wife, Desiree laughs as she holds their son, Jonathan, in November. Larson's amputation came after suffering complications from injuries in an IED explosion that hit his Humvee in Iraq.</b></h5>
			  <h5 class="caption">(Photo: Kimm Anderson, kanderson@stcloudtimes.com)</h5>
