			<p>Tony was flown to Camp Anaconda, where he underwent eight hours of surgery, then was moved to Germany. After four days, he arrived at Walter Reed Army Medical Center in Washington, D.C.</p>

			<p>It would be his home for the next 18 months.</p>

			<p>The shrapnel had pierced the middle of his foot and broken eight bones — shattered them until they were unrecognizable fragments, really. He was missing tendons, ligaments, muscles, nerves. The only thing holding his toes up was skin.</p>

			<p>For the next nine months, Tony fought to save his foot. He made progress, moving to a walking cast then to a soft cast that could bear his full weight.</p>

			<p>Still, his surgeon warned him that eventually his foot might need to come off. The medications they gave him helped manage the pain, but never really got rid of it. The prognosis was grim — multiple surgeries, bones fused together. He would only be able to walk short distances or stand for a short time.</p>

			<p>Then, in mid-May 2006, came bad news. He had osteomyelitis, a bone infection.</p>

			<p>Still, there was no medical urgency to remove his foot. No one would tell him what he should do — not his doctors, not his family. It was an agonizing decision, and he had to make it alone.</p>

			<p>On July 26, 2006, Tony underwent yet another surgery. This time, doctors cut off the foot he’d fought so long and hard to save. His leg was amputated about halfway between the knee and ankle.</p>

			<p><i><b>I could still feel the foot burning. I could feel itches and tickles and all those fun little phantom sensations. ... Then the phantom pains started to settle in. I could feel the wound track from the shrapnel really beginning to burn and I could feel the arch of my foot collapsing and the joint hurting all the time.</b></i></p>

			<p><i><b>And they weren’t there.</b></i></p>

			<p>Life became an endless schedule of daily physical and occupational therapy. He got his first prosthetic foot in August 2006. Then came the slow process of learning to walk again.</p>

			<p>Taking the first step, Tony felt strange and uncertain, unsure how much to trust the hardware strapped to his leg. It took almost six months to feel somewhat comfortable in his gait and balance.</p>

			<p>The Army gave Tony a choice: Stay in and take a different job, or take a medical discharge. He considered the idea of sitting behind an Army desk for 13 years, watching unit after unit deploy while he stayed home. He decided he couldn’t handle that.</p>

			<p>So on April 27, 2007, Tony was discharged from Walter Reed — and from military life.</p>

			<p>A part of him was relieved that he’d fulfilled his end of the contract. But he would miss the camaraderie, hanging out with the guys during weekend drills, shooting the breeze while they fixed a truck.</p>

			<p>The following day, he stepped off a plane at St. Cloud Regional Airport. Waiting for him were hundreds of people — local dignitaries, members of his unit, police officers, firefighters, media crews and 217 motorcycles with their Patriot Guard Riders.</p>

			<p>The support was overwhelming. The day passed in a blur.</p>

			<p>Tony was home, but not whole. The healing was just beginning.</p>

			<p>While at Walter Reed, Tony was diagnosed with post-traumatic stress disorder and a traumatic brain injury suffered during the blast.</p>

			<p>Later there were other problems: Depression, anxiety, sleep apnea, forgetfulness, difficulty concentrating.</p>

			<p>Then came the migraine headaches, sometimes daily. They are debilitating, leaving him dead to the world, shut in a dark, cool room, unable to even move.</p>

			<p>The PTSD brings flashbacks, often while he is driving or riding in a car. He is hypervigilant, constantly looking over his shoulder, watching the vehicles ahead, behind and on every side, looking in ditches, avoiding manhole covers or garbage alongside the road. It’s as if the war never ended.</p>

			<p>Then in December 2006, when Tony was attending a ski event for wounded veterans in Breckenridge, Colorado, the chief training officer for America’s VetDogs asked if he’d ever considered getting a service dog.</p>

			<div class="large-4 medium-4 right column panel" style="margin-left:10px">
			  <img src="assets/img/p1/submitted/larsonsub7.jpg">
			  <h5><b>Tony Larson with service dog Tomme.</b></h5>
			  <h5 class="caption">(Submitted photo)</h5>
			</div>

			<p>Tony responded no, he wasn’t blind. But the training officer explained that service dogs could help with a variety of daily tasks for people with a range of disabilities.</p>

			<p>In April 2007, a burly black Lab named Tomme entered his life, trained specifically for Tony’s needs. Tomme provides steady support when Tony is getting to his feet and helps him balance as he walks. He can pick up a dropped wallet or cellphone.</p>

			<p>He perceives Tony’s moods with an eerie sixth sense. While riding in the car, Tomme senses when Tony is having a flashback. He climbs over the console and lays his big head in Tony’s lap, his hot breath and warm snout bringing Tony back to reality.</p>

			<p>It’s OK. You’re home.</p>

			<div class="large-12 medium-12 column panel">
			  <img src="assets/img/p1/larson/larson8.jpg">
			  <h5><b>Tony and Desiree Larson's son, Jonathan, plays with hanging baskets of potatoes in their St. Cloud home. Desiree, a rape survivor who also has PTSD, found understanding in her relationship with Tony.</b></h5>
			  <h5 class="caption">(Photo: Kimm Anderson, kanderson@stcloudtimes.com)</h5>
			</div>

			<p><i><b>The PTSD, the effects of traumatic brain injury, all the miscellaneous physical effects — it’s a roller coaster. There’s days and weeks and months and years when things are good. Then he can go days and weeks and months and years when things are not good at all.</b></i></p>

			<p>Tara Woolery, known to friends by her childhood nickname Desiree, had known Tony since their high school days in Sartell. They had mutual friends and talked a lot, but never dated.</p>

			<p>Tony had a different girlfriend during his deployment, a relationship that soured after he returned.</p>

			<p>Desiree was in the Patriot Guard at the airport the day Tony came home. She’d heard who it was and wanted to be sure she was there.</p>

			<p>One night, Tony’s former college roommate, a good friend of Desiree’s, suggested they invite her over. They began seeing each other more often.</p>

			<p>Desiree has her own struggles. She is a rape survivor with post-traumatic stress disorder. In Tony, she saw the same symptoms. She also found understanding.</p>

			<p><i><b>He was the first guy that I had ever been around in a long time since I had been raped that just understood it when I said there was something that was triggering me or I was having an off day. He got it. I never had to explain, and he was always very patient about that. And for me, it was the same way. He never had to explain himself to me. I just understood why.</b></i></p>

			<div class="large-4 medium-4 column right panel" style="margin-left:10px;">
			  <img src="assets/img/p1/larson/larson3.jpg" style="width:80%">
			  <h5><b>With a traumatic brain injury, remembering to do simple tasks becomes more difficult for Tony Larson. He uses this board to keep track.</b></h5>
			  <h5 class="caption">(Photo: Kimm Anderson, kanderson@stcloudtimes.com)</h5>
			</div>

			<p>Eventually, she moved into his small house in north St. Cloud and was shocked at the conditions he’d been living in. The house was unkempt, cluttered with unwashed dishes and unpaid bills.</p>

			<p>Sometimes Tony would forget to shower for several days, then take three showers in a one day because he forgot he’d taken one. He would skip his medications, unable to recall if he’d already taken them and afraid to take a double dose. He would become engrossed in a project and forget to eat.</p>

			<p><i><b>I was absolutely appalled that somebody who had done so much in his life was living like this, and that there was so few people who seemed to see what was actually going on. I think I now understand how much he shut people out of that part. But I vowed that never again would he live like that as long as we were together.</b></i></p>

			<p>She cleaned, cooked, paid the bills. She began going along to Tony’s many appointments because he often forgets what the doctor said by the time he gets home.</p>

			<p>A magnetic calendar hangs on the refrigerator, color-coded for each person’s appointments. A whiteboard hangs in the bathroom with reminders for Tony: Brush teeth. Shave. Comb hair.</p>

