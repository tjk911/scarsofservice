			<p>He’s learned ways of coping with those bad days — a long walk, or watching TV for half a day. Tomme’s calming presence helps.</p>

			<p>He and Desiree have had serious talks about suicide. There are no guns in the house. She conceals the keys for the medication dispenser in various hiding places around the house.</p>

			<p><i><b>It’s really hard when somebody that you love so much tells you that they would be better off, that you would be better off if they just were gone. It’s hard to get through that wall and convince them that, no, your life is worth living.</b></i></p>

			<p>One of those reasons is Jonathan, a spikey-haired toddler with wide blue eyes born in March 2013.</p>

			<p>He’s the center of the household, crawling around the floor, sitting in his dad’s lap or hanging out contentedly in a sling strapped across Desiree’s body as she cooks dinner.</p>

			<p>Desiree knew many of the parenting duties would fall on her, and she accepted it. It seemed to her that everything they’d been through shouldn’t preclude them from being a family and being happy.</p>

			<p>Tony helps change diapers and plays with Jonathan. But there are days when he can’t carry his son because he’s off his prosthesis due to an ingrown hair or a scratch that causes soreness and the risk of infection.</p>

			<p>Once a social extrovert, the 32-year-old Tony now finds crowds make him tense. He prefers to stay home while Desiree takes Jonathan to the park or a music festival.</p>

			<p><i><b>Sometimes I feel in just this small little corner of my heart I kind of mourn the loss of Tony being able to participate in some of that stuff, when he’s not feeling the greatest and feeling that he can.</b></i></p>

			<p><i><b>But here at home, Jonathan is Daddy’s boy.</b></i></p>

			<p>In July, Desiree gave birth to another baby boy named Elijah.</p>

