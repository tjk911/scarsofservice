			<p>From elementary school, Tony Larson knew he wanted to be in the military. It was in his blood.</p>

			<p>His dad was one of seven boys, and six of them served. Four fought in Vietnam.</p>

			<p>It was an honor thing, a way to further his education, become a more well-rounded person. In high school, Tony spent hours researching the different military branches. He chose the National Guard because he liked the focus on state missions instead of national and international ones.</p>

			<p>When he turned 17, he asked his parents to sign papers so he could join the Guard and go to basic training between his junior and senior years. But their mental image of the military was haunted by Vietnam. They refused.</p>

			<div class="large-3 medium-4 column right panel" style="margin-left:10px;">
			  <img src="assets/img/p1/submitted/larsonsub1.jpg">
